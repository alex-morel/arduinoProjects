#ifndef VGI_h
#define VGI_h

#include <inttypes.h>

typedef struct{
  uint8_t Multiplier; //             | $00-$0F                            |
  uint8_t Detune; //                 | $00-$06                            |
  uint8_t Total_Level; //            | $00-$3F                            |
  uint8_t Rate_Scaling; //           | $00-$03                            |
  uint8_t Attack_Rate; //            | $00-$1F                            |
  uint8_t Decay_Rate_AM_enable; //   | $00-$1F, $80-$9F                   |
  uint8_t Sustain_Rate; //           | $00-$1F                            |
  uint8_t Release_Rate; //           | $00-$0F                            |
  uint8_t Sustain_Level; //          | $00-$0F                            |
  uint8_t SSG_EG; //                 | $00, $08-$0F                       |

} VGI_OP_t;


typedef struct{
  uint8_t Algorithm; //                  | $00-$07                            |
  uint8_t Feedback; //                   | $00-$07                            |
  uint8_t AMD_FMD; //                    | $00-$07, $10-$17, $20-$27, $30-$37 |
  VGI_OP_t  ops[4];
} VGI_t;


#endif


